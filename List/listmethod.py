#list methods

list = [1,2,3,4,1]
list.append(5) #add element in list
print(list)

list.sort() #sort in ascending order
print(list)

list.sort(reverse=True) #sort the list in dencending order
print(list)

list.reverse() #reverses the list
print(list)

list.insert(2,69) #insert element in any index in list
print(list)

list.remove(1) #remove fist copy element in list
print(list)

list.pop(4)# remove element from list
print(list)